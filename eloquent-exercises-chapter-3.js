// MINIMUM

// Build a function that returns its smallest argument

// Define min to hold a function value
const min = function(a, b) {
	if (a < b) {
		return a;
	} else {
		return b;
	}
}

// Declare g to be a function
function min(c, d) {
	if (c < d) {
		return c;
	} else {
		return d;
	}
}

// A less verbose function value
min = (e, f) => {
	if (e < f) {
		return e;
	} else {
		return f;
	}
}

